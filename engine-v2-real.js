// 4. 自主循环控制器
// ============================================

async function autonomousLoop(projectPath, options = {}) {
  const opts = {
    task: options.task || 'Implement feature',
    maxRounds: CONFIG.maxRounds,
    minRounds: CONFIG.minRounds,
    interactive: options.interactive || false,
    ...options
  };
  
  echo(`\n${'═'.repeat(60)}`);
  echo(`🔄 启动自主进化循环`);
  echo(`${'═'.repeat(60)}`);
  echo(`📁 项目路径：${projectPath}`);
  echo(`📝 任务：${opts.task}`);
  echo(`🎯 质量目标：Critical=${CONFIG.targetQuality.critical}, High=${CONFIG.targetQuality.high}`);
  echo(`🔄 轮次范围：${opts.minRounds}-${opts.maxRounds}`);
  echo(`${'═'.repeat(60)}\n`);
  
  // 初始化策略
  let strategy = { version: 1.0, task: opts.task, round: 0 };
  let round = 0;
  const evolutionHistory = [];
  
  while (true) {
    round++;
    strategy.round = round;
    
    echo(`\n${'─'.repeat(60)}`);
    echo(`🔄 Round ${round}/${opts.maxRounds}`);
    echo(`${'─'.repeat(60)}\n`);
    
    try {
      // 1. Generator 实现
      const genResult = await generate(projectPath, strategy);
      
      // 2. Discriminator 审查
      const review = await discriminate(projectPath);
      
      // 3. 质量检查
      const qualityMet = review.criticalHigh === CONFIG.targetQuality.critical &&
                         round >= opts.minRounds;
      
      echo(`\n📊 质量检查结果:`);
      echo(`   Critical/High: ${review.criticalHigh} (目标：${CONFIG.targetQuality.critical})`);
      echo(`   轮次：${round} (最少：${opts.minRounds})`);
      echo(`   质量达标：${qualityMet ? '✅' : '❌'}`);
      
      if (qualityMet) {
        echo(`\n✅ 质量目标达成！进化完成。\n`);
        break;
      }
      
      if (round >= opts.maxRounds) {
        echo(`\n⚠️ 达到最大轮次限制。进化完成。\n`);
        break;
      }
      
      // 4. Evolver 更新策略
      const newStrategy = await evolve(review, strategy);
      strategy.version = parseFloat(newStrategy.version);
      
      // 记录进化历史
      evolutionHistory.push({
        round,
        version: strategy.version,
        issues: review.issues.length,
        criticalHigh: review.criticalHigh
      });
      
      // 5. 交互模式等待确认
      if (opts.interactive) {
        const answer = await question('\n继续下一轮进化？(Y/n) ');
        if (answer.toLowerCase() === 'n') {
          echo('\n⚠️ 用户终止进化。\n');
          break;
        }
      }
      
    } catch (error) {
      echo(`\n❌ Round ${round} 失败：${error.message}`);
      echo(`\n⚠️ 终止进化循环。\n`);
      break;
    }
  }
  
  // 生成最终报告
  await generateFinalReport(round, strategy, evolutionHistory);
  
  echo(`\n${'═'.repeat(60)}`);
  echo(`🎉 自主进化循环完成！`);
  echo(`${'═'.repeat(60)}`);
  echo(`📊 总轮次：${round}`);
  echo(`📈 最终策略：v${strategy.version}`);
  echo(`📁 进化报告：EVOLUTION-REPORT.md`);
  echo(`${'═'.repeat(60)}\n`);
}

// ============================================
// 工具函数
// ============================================

async function getChangedFiles(projectPath) {
  try {
    cd(projectPath);
    const { stdout } = await $`git diff --name-only HEAD 2>/dev/null || find src -name "*.ts" -o -name "*.tsx" -o -name "*.js" -o -name "*.jsx" | head -20`;
    return stdout.trim().split('\n').filter(Boolean);
  } catch (e) {
    return ['src/index.ts'];
  }
}

function parseESLintOutput(output) {
  const issues = [];
  const lines = output.split('\n');
  
  for (const line of lines) {
    if (line.includes('error') || line.includes('warning')) {
      issues.push({
        severity: line.includes('error') ? 'High' : 'Low',
        description: line.trim(),
        file: 'N/A',
        line: 0
      });
    }
  }
  
  return issues.slice(0, 10);
}

function identifyPatterns(issues) {
  const patternMap = new Map();
  
  for (const issue of issues) {
    const key = issue.description.split(' ').slice(0, 5).join(' ');
    patternMap.set(key, (patternMap.get(key) || 0) + 1);
  }
  
  return Array.from(patternMap.entries())
    .filter(([_, count]) => count >= 1)
    .map(([name, count]) => ({ name: `P${Date.now()}-${name.substring(0, 10)}`, count }));
}

