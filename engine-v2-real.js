// 3. Evolver - 真正的策略进化 (AI Agent 集成)
// ============================================

async function evolve(review, strategy) {
  echo(`\n🧬 Evolver: 更新进化策略`);
  
  try {
    // 1. 识别问题模式
    const patterns = identifyPatterns(review.issues);
    echo(`   📊 识别 ${patterns.length} 个问题模式`);
    
    // 2. 构建 AI Agent 提示词
    const evolvePrompt = `
你是 Evolver(进化器)，负责更新进化策略。

## 当前策略版本
v${strategy.version}

## 新发现问题
${review.issues.length} issues
Critical/High: ${review.criticalHigh}

## 识别的问题模式
${patterns.map(p => `- ${p.name} (${p.count}次)`).join('\n')}

## 任务
1. 更新策略文档到 v${(strategy.version + 0.1).toFixed(1)}
2. 添加新问题模式到模式库
3. 记录进化日志

## 输出格式 (JSON)
{
  "newVersion": "v1.1",
  "changes": [
    "新增规则：...",
    "修改规则：...",
    "删除规则：..."
  ],
  "newPatterns": ["P001", "P002"],
  "summary": "进化总结"
}

现在请更新策略：
`.trim();
    
    // 3. 调用 AI Agent
    const evolution = await callAIAgent('Evolver', evolvePrompt);
    
    // 4. 写入策略文档
    const newVersion = evolution.newVersion || (strategy.version + 0.1).toFixed(1);
    echo(`   📝 写入策略文档 v${newVersion}...`);
    
    const strategyPath = join(CONFIG.workspace, CONFIG.memoryDir, 'strategy', `v${newVersion}.md`);
    await fs.mkdir(dirname(strategyPath), { recursive: true });
    
    const strategyContent = `# Evolution Strategy v${newVersion}\n\n## 变更\n\n${(evolution.changes || []).map(c => `- ${c}`).join('\n')}\n\n## 总结\n\n${evolution.summary || '策略更新完成'}`;
    await fs.writeFile(strategyPath, strategyContent);
    echo(`      ✅ ${strategyPath}`);
    
    // 5. 更新模式库
    echo(`   📚 更新问题模式库...`);
    const patternsPath = join(CONFIG.workspace, CONFIG.memoryDir, 'patterns');
    await fs.mkdir(patternsPath, { recursive: true });
    
    for (const pattern of patterns) {
      const patternFile = join(patternsPath, `${pattern.name}.md`);
      await fs.writeFile(patternFile, `# Pattern: ${pattern.name}\n\n出现次数：${pattern.count}\n\n描述：自动识别的问题模式`);
    }
    echo(`      ✅ 添加 ${patterns.length} 个新模式`);
    
    // 6. 记录进化日志
    echo(`   📝 记录进化日志...`);
    const logPath = join(CONFIG.workspace, CONFIG.memoryDir, 'logs', `${new Date().toISOString().split('T')[0]}-round-${strategy.round}.md`);
    await fs.mkdir(dirname(logPath), { recursive: true });
    
    await fs.writeFile(logPath, JSON.stringify({
      timestamp: new Date().toISOString(),
      round: strategy.round,
      oldVersion: strategy.version,
      newVersion: newVersion,
      changes: evolution.changes,
      patterns: evolution.newPatterns || [],
      issuesFound: review.issues.length,
      criticalHigh: review.criticalHigh
    }, null, 2));
    echo(`      ✅ ${logPath}`);
    
    echo(`   🧬 策略进化完成：v${strategy.version} → v${newVersion}\n`);
    
    return { version: newVersion, changes: evolution.changes || [] };
    
  } catch (error) {
    echo(`   ❌ Evolver 失败：${error.message}\n`);
    throw error;
  }
}

